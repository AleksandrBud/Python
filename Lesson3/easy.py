# Задание - 1
# Создайте функцию, принимающую на вход Имя, возраст и город проживания человека
# Функция должна возвращать строку вида "Василий, 21 год(а), проживает в городе Москва"
def people_info(i_name, i_age, i_city):
    return ("{}, {} год(а), проживает в городе {}").format(i_name, i_age, i_city)

name = 'Василий'
age = 21
city = 'Москва'

print(people_info(name,age,city))
