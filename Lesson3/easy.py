# Создайте функцию, принимающую на вход 3 числа, и возвращающую наибольшее из них
def max_digit(i_dig_1,i_dig_2,i_dig_3):
    return max(int(i_dig_1), int(i_dig_2), int(i_dig_3))
dig_1 = 4
dig_2 = 16
dig_3 = 1

print(max_digit(dig_1,dig_2,dig_3))
# Задание - 3
# Создайте функцию, принимающую неограниченное количество строковых аргументов,
# верните самую длинную строку из полученных аргументов
def  max_string(*args):
    return max(args, key=len)
arg = 'sadas'
arg_1 = 'asd'
arg_2 = 'sdkjskljgs'
arg_3 = 'd'
print(max_string(arg, arg_1, arg_2, arg_3))